export const isDE = (navigator.language || navigator.userLanguage).toLowerCase().startsWith('de')
export const lang = isDE ? 'de': 'en';

const ucfirst = (s) => {
  if (typeof s !== 'string') return ''
  return s.charAt(0).toUpperCase() + s.slice(1)
}

export const text_anno = (id) => {
  id = typeof id === "string" ? parseInt(id.replace(/_\d/, '')) : id
  let string
  string = anno_strings[lang][id]
  if (string) return string

  string = anno_strings['de'][id] || anno_strings['en'][id]
  if (string) return `{${id}} ${string}`

  return `{${id}}`
}

export const text_plan = (id, attr = undefined) => {
  const string = plan_strings[lang][id] || `{${id}}`
  if (undefined !== attr) {
    return string.replace('{attr}', attr)
  }
  return string
}
export const text_plan_ucf = (id, attr = undefined) => ucfirst(text_plan(id, attr))
